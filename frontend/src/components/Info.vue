    <h2 id="S1">1. PELIN PERUSIDEA</h2>

    <p>Pelin ideana on saada kyykät pois pelineliöstä heittämällä niitä kartuilla. Eniten kyykkiä pelineliöstä poistanut joukkue on voittaja.</p>

    <h2 id="S2">2. PELIKENTTÄ</h2>

    <h3>Kyykkäkenttä</h3>

    <p>Mahdollisimman tasainen alue, kooltaan 5 x 20 metriä, johon on piirretty 5 x 5 metrin pelineliöt. Pelineliöiden väli on 10 metriä.</p>

    <h2 id="S3">3. PELIVÄLINEET</h2>

    <h3>3.1. Kyykät</h3>

    <p>Kyykät tehdään puusta sylinterin muotoisiksi. Kyykän korkeus on 10 cm ja halkaisija 6 - 8 cm. Kyykkiä tarvitaan joukkuepeliin 80 kappaletta eli 20 paria kummankin pelineliön eturajalle. Myös varakyykkiä on hyvä olla rikkoutumisen varalle.</p>

    <h3>3.2. Kartut</h3>

    <p>Kartut ovat varreltaan pyöreitä ja niissä on kädensija. Kartut eivät saa olla 85 cm:iä pidempiä eivätkä 8 cm:iä paksumpia. Kartun saa maalata tai sitä muuten vahvistaa kestävyyden tai lyöntiominaisuuksien parantamiseksi. Karttuja tarvitaan joukkuepelissä 4 heittovuoroa kohden. Akateemisen kyykän MM-kilpailut tarjoaa peleihin kartut. Omia karttuja saa käyttää, mutta tällöin on annettava myös vastajoukkueen käyttää niitä.</p>

    <h2 id="S4">4. PELIN NIMITYKSIÄ</h2>

    <h3>Heittoneliö</h3>

    <p>Neliö, josta heitot suoritetaan.</p>

    <h3>Pelineliö</h3>

    <p>Neliö, jossa ovat ulosheitettävät kyykät.</p>

    <h3>Akka</h3>

    <p>Pelineliöön tai sen eturajalle jäänyt kyykkä</p>

    <h3>Pappi</h3>

    <p>Pelikentän ulkorajalle yli 10 cm päähän eturajasta jäänyt kyykkä</p>

    <h3>Kuokkavieras</h3>

    <p>Pelineliöiden väliin ponnahtanut kyykkä</p>

    <h3>Pääty</h3>

    <p>Pelissä pelataan kaksi päätyä, joiden välissä suoritetaan puoltenvaihto. Päätyyn kuuluu 2 heittovuoroa pelaajaa kohden.</p>

    <h2 id="S5">5. JOUKKUE</h2>

    <p>Kyykkäjoukkue muodostuu neljästä pelaajasta. Akateemisissa peleissä pelaajia on mahdollista vaihtaa kesken sarjan, mutta ei kesken ottelun. Naisten sarjassa kaikkien pelaajien on oltava naisia.</p>

    <h2 id="S6">6. TUOMARIT</h2>

    <p>Kilpailun tuomareina toimivat pelituomarit, aluetuomarit ja päätuomarit. Tuomarit ovat aina oikeassa ja heitä tulee auttaa, mikäli he niin pyytävät.</p>

    <h3>6.1. Pelituomari</h3>

    <p>Jokaisesta kenttää varten on pelituomari, joka tuomitsee rehdisti, oikeudenmukaisesti ja lahjonnasta huolimatta valppaasti. Pelituomari valvoo, tuomitsee ja kirjaa pelitulokset sääntöjen ohjaamana. Mikäli pelituomarin toiminnasta on valittamista, valitukset tulee osoittaa päätuomarille. Mikäli pelituomari menee rikki, saa joukkue vaihdossa uuden tuomaritoimistosta.</p>

    <h3>6.2. Aluetuomari</h3>

    <p>Aluetuomarit vastaavat pelien sujuvuudesta ja ratkaisevat mahdolliset erimielisyydet omalla kenttäalueellaan.</p>

    <h3>6.3. Päätuomari</h3>

    <p>Kilpailua johtaa yksi tai useampi päätuomari. Päätuomarit johtavat aluetuomareiden ja pelituomareiden toimintaa sekä ratkaisevat merkittävimmät erimielisyydet peleissä.</p>

    <h2 id="S7">7. PELIN PYSTYTYS</h2>

    <p>Jokaisen päädyn alussa kummankin neliön eturajalle pystytetään 20 paria päällekkäisiä kyykkiä tasaisin välein. Rivin kumpaankin reunaan on jätettävä 10 cm tyhjää tilaa. Ennen pelin alkamista saavat pelaajat huoltaa pelikenttää. Pelin aikana pelikenttää saa huoltaa vain tuomari.</p>

    <p>Mikäli pelin aikana kyykkäkentälle ilmestyy sinne kuulumattomia asioita, kuten kyykkiä tai ei-heittovuorossa olevia pelaajia, on tuomarin tehtävä poistaa ne.</p>

    <p>Pelaajat huolehtivat aina kyykkäkentän valmiiksi seuraavaa peliä tai päätyä varten.</p>

    <h2 id="S8">8. PELIN KULKU</h2>

    <h3>8.1. Hutunkeitto</h3>

    <p>Hutunkeitto tapahtuu seuraavasti: Pelituomari kutsuu kummankin joukkueen edustajan luokseen kentälle. Toinen edustaja heittää kartun pelituomarille. Tämän jälkeen kumpikin edustaja vuorotellen asettaa kätensä siten, että pikkusormi koskettaa vastustajan etusormea ja peukaloa. Kaikkien otteiden on oltava samanlaisia. Viimeisen otteen saaneen pelaajan ei tarvitse pyöräyttää karttua pään ympäri voiton osoitukseksi, mutta otteen on oltava pitävä. Arvonnan voittanut päättää joko kenttäpuoliskot tai aloittavan joukkueen. Häviäjä saa päättää jäljelle jääneestä vaihtoehdosta.</p>

    <h3>8.2. Pelaaminen</h3>

    <h4>8.2.1. Heitto</h4>

    <p>Heitto alkaa siitä, kun karttu irtoaa pelaajan kädestä ja loppuu siihen, kun karttu poistuu pelikentältä tai pelivälineet ovat pysähtyneet. Akateemisissa kisoissa kartun heittoa saa avustaa toisella kädellä, ellei voima muuten riitä.</p>

    <h4>8.2.2. Avausheitto</h4>

    <p>Miehet avaavat heittämällä ensimmäisen kartun heittoneliön takarajalta, naiset eturajalta. Pelin avaus katsotaan tapahtuneeksi, kun ensimmäinen kyykkä poistuu pelineliöstä. Kuokkavierasta tai pappia ei hyväksytä ulosmenneeksi. Heitot suoritetaan tällä tavalla, kunnes joukkue on onnistuneesti avannut pelin.</p>

    <p>Joukkueen suoritettua avauksen miehet siirtyvät eturajalle ja naiset 2 m heittoneliön eturajasta heittosuuntaan.</p>

    <h4>8.2.3. Heittovuoro</h4>

    <p>Joukkueen heittovuoron aikana kaksi joukkueen pelaajaa heittää kumpikin kaksi kertaa, eli yhteensä joukkue heittää neljä karttua. Pelaaja heittää heittovuoronsa kartut perätysten. Heittovuoron ajan on pelaajan pidettävä karttunsa heittoneliössä. Vain pelituomarin luvalla sallitaan kartun vaihto heittoneliöstä.</p>

    <p>Jos karttu jää pelineliöön, sitä ei saa poistaa ennen joukkueen heittovuoron loppumista. Kerran pelineliöstä onnistuneesti poistettu kyykkä lasketaan poistuneeksi, vaikka se olisi syystä tai toisesta vierinyt sinne takaisin. Tuomari poistaa pelikentälle palanneet kyykät.</p>

    <h4>8.2.4. Kentän tarkastaminen</h4>

    <p>Heittovuoron (2 pelaajaa, 4 karttua) jälkeen pelituomari tarkastaa pelineliön, toteaa ulos menneet, rajalla olevat ja pelineliöön jääneet kyykät sekä antaa luvan poistaa kartut pelineliöstä. Tarkastuksen jälkeen tuomari antaa toiselle joukkueelle heittoluvan.</p>

    <h4>8.2.5.Vuorojen eteneminen</h4>

    <p>
      Pelivuorot jatkuvat, kunnes jokainen pelaaja on heittänyt yhteensä 4 karttua eli on pelattu yksi pääty. Yhden päädyn aikana joukkueen heittojärjestyksen on pysyttävä samana.&nbsp;
      <br>Päädyn jälkeen tuomari laskee ja merkitsee ylös ensimmäisen päädyn pisteet sekä määrää puoltenvaihdon. Tämän jälkeen pelataan toinen pääty. Puoltenvaihdossa aloitusvuoro vaihtuu. Puoltenvaihdon yhteydessä saa joukkue halutessaan vaihtaa pelaajien heittojärjestystä.
    </p>

    <h3>8.3. Pelin päättäminen</h3>

    <p>Toisen päädyn jälkeen tuomari laskee yhteen päätyjen pisteet ja julistaa voittajaksi sen joukkueen, jolla on vähiten miinuspisteitä.</p>

    <h2 id="S9">9. ERIKOISTILANTEET</h2>

    <h3>9.1. Pelaajan häirintä</h3>

    <p>Heittovuorossa olevaa pelaajaa saa kohtuudella häiritä pelikentän ulkopuolelta. Tämä koskee myös heittäjän omaa joukkuetta.</p>

    <h3>9.2. Yliastuttu heitto</h3>

    <p>Heiton aikana pelaaja ei saa ylittää viivaa, jolta heitto tapahtuu. Jos heitto on yliastuttu, tuomari asettaa ulosmenneet kyykät takaisin pelineliön eturajalle, keskeltä aloittaen. Neliöön kaatuneet jäävät paikoilleen. Heitto katsotaan tapahtuneeksi.</p>

    <h3>9.3. Vauhdinotto</h3>

    <p>Vauhdinotto on sallittua heittorajan takaa kohdan 8 sääntöjen puitteissa. Vauhdinoton aikana ei saa siirtää vastustajan kyykkiä.</p>

    <h3>9.4. Viivyttely</h3>

    <p>Tarpeeton viivyttely heitoissa on kiellettyä. Jos joukkue toistuvasti ja huomautuksesta huolimatta viivyttelee heitoissaan, voi tuomari määrätä joukkueelle yhteisajan jonka kuluessa heittovuoron loput kartut on heitettävä. Aika saadaan kertomalla karttujen määrä 30 sekunnilla.</p>

    <h3>9.5. Haljennut kyykkä</h3>

    <p>Jos kartun osuma halkaisee kyykän, pelituomari tuomitsee sen mukaan, minne isompi osa kyykästä päätyi. Samalla hän vaihtaa rikkoutuneen kyykän ehjään. Kyykän haljetessa yhtä suuriin osiin ulosmennyt osa ratkaisee. Yhtä suurten osien jäädessä pelineliöön pelaaja ratkaisee, kumman kyykänosan kohdalle ehjä kyykkä asetetaan.</p>

    <h3>9.6. Kyykkien siirtely päädyn pelin aikana</h3>

    <p>Vastustajan kyykkiä ei saa siirtää. Jos pelaaja potkii, heittää tai muuten siirtää pelialueella olevia kyykkiä, antaa pelituomari siirtelijälle varoituksen. Kahden varoituksen jälkeen tuomari määrää siirtelijän heittämään takarajalta loppupelin ajaksi. Jos vastustajan kyykät siirtyvät ulos, ne tulkitaan sinne jääneiksi. Jos ne jäävät pelineliöön tai pelineliöiden väliin, tuomari nostaa ne takaisin paikoilleen.</p>

    <h3>9.7. Pelistä myöhästyminen</h3>

    <p>Myöhästymistilanteessa tuomari voi omalla harkinnallaan päättää pelattavaksi vain puolikkaan pelin. Puolikas peli pelataan kuten normaalipeli, mutta joukkueella on käytössään vain 8 karttua / puoliaika.</p>

    <p>
      Mikäli joukkue ei ole kokonaisuudessaan pelikentällä 15 minuutin kuluessa pelin määrätystä alkamisajankohdasta, voi
      <b>päätuomari</b> julistaa joukkueen keskeyttäneeksi ja vastustajan saaneen luovutusvoiton.
    </p>

    <h3>9.8. Tasapeli jatkopeleissä</h3>

    <p>Tasapeli pudotuspeleissä johtaa lyhyeen uusintaan. Toiselle kenttäpuoliskolle asetetaan neljä kyykkäparia eturajalle metrin välein aloittaen metri sivurajasta.</p>

    <p>
      * Joukkueet pelaavat samaan päätyyn. Ensin varsinaisen pelin aloittanut joukkue, sitten toinen. Välissä lasketaan pisteet ja kyykät pystytetään uudelleen.
      <br>* Kukin joukkueen pelaaja heittää yhden kartun. Joukkue saa itse määrätä heittojärjestyksen.
      <br>* Pisteet lasketaan kohdan 10.1. mukaisesti.
      <br>* Uusinnan voittaja on se joukkue, jonka tulos on parempi.
    </p>

    <h3>9.9. Pelaajan valitusoikeus</h3>

    <p>Kun joukkue huomaa vastustajan tehneen sääntöjen vastaisen heiton tai muun toimenpiteen, niin he heti tästä protestoivat ja pyytävät ratkaisua. Yksikin heitto rikkeen tapahtumisen jälkeen merkitsee valitusoikeuden menettämistä. Tuomarin pitää antaa valituksen jälkeen lupa jatkaa peliä.</p>

    <h2 id="S10">10. PISTELASKU JA PELIN VOITTAJA</h2>

    <h3>10.1. Pelipisteet</h3>

    <ul>
      <li>Eturajalle tai neliöön jäänyt kyykkä (akka) -2</li>
      <li>Kyykkä neliöiden välissä (kuokkavieras) -2</li>
      <li>Kyykkä pelineliön rajalla (pappi) -1</li>
      <li>Heittämättä jäänyt karttu +1</li>
    </ul>
    <h3>10.2. Lohkon pisteytys</h3>

    <p>Voitosta saa 2 pistettä, tasapelistä 1 pisteen ja häviöstä 0 pistettä.</p>

    <h3>10.3. Lohkon voittaja</h3>

    <ul>
      <li>Eniten pisteitä lohkosta saanut voittaa lohkon.</li>
      <li>Jos joukkueilla on yhtä paljon voittopisteitä, ratkaisee keskinäisten otteluiden tulos.</li>
      <li>Jos keskinäisten otteluiden voittopisteet ovat tasan, ratkaisee keskinäisissä otteluissa saatujen pelipisteiden keskiarvo.</li>
      <li>Jos nämäkin ovat tasan, ratkaisee joukkueen lohkon kaikissa otteluissa saamien pelipisteiden keskiarvo.</li>
      <li>Jos nämäkin ovat tasan, ratkaisee voittajan arpa.</li>
    </ul>
